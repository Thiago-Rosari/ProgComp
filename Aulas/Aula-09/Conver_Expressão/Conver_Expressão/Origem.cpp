// conversões em expressões
#include <iostream>
using namespace std;

int main()
{
	long long a, b, c, d;

	a = 200903280945;
	b = 100000000 * 2009;
	c = 100000000LL * 2009;
	d = 10000000000 * 2009;

	cout << "a = " << a << endl;
	cout << "b = " << b << endl;
	cout << "c = " << c << endl;
	cout << "d = " << d << endl;

	return 0;
}